package com.scheffsblend.smw;

/**
 * This class has some handy keycodes we can send to the SDL app
 * 
 * @author Clark Scheff
 *
 */

public final class KeyCodes {
	// These are the scan code values found in SDL_scancode.h
	public static final int KEY_ENTER		= 40;
	public static final int KEY_ESCAPE		= 41;
	public static final int KEY_RIGHTARROW	= 7;
	public static final int KEY_LEFTARROW	= 4;
	public static final int KEY_DOWNARROW	= 22;
	public static final int KEY_UPARROW		= 26;
	public static final int KEY_RCTRL		= 8;
	public static final int KEY_RSHIFT		= 20;
}
